def main():
    parser = argparse.ArgumentParser(description="AIOS Kernel")
    parser.add_argument("--project-root", default=os.getcwd())
    sub = parser.add_subparsers(dest="command")

    sub.add_parser("status", help="Show kernel status")

    p_agent = sub.add_parser("agent", help="Agent management")
    p_agent.add_argument("action", choices=["list", "register", "mode"])
    p_agent.add_argument("--name", help="Agent name")
    p_agent.add_argument("--mode", choices=["manual", "assisted", "autonomous"])

    p_sched = sub.add_parser("scheduler", help="Scheduler management")
    p_sched.add_argument("action", choices=["list", "due"])

    args = parser.parse_args()

    kernel = create_default_kernel(args.project_root)

    if args.command == "status":
        status = kernel.get_status()
        print(json.dumps(status, indent=2, default=str))
    elif args.command == "agent":
        if args.action == "list":
            for config in kernel.agent_manager.get_all():
                print(f"  {config.name:<16} mode={config.mode.value:<12} caps={config.capabilities}")
        elif args.action == "register" and args.name:
            config = AgentConfig(name=args.name, mode=AgentMode(args.mode or "assisted"))
            kernel.agent_manager.register(config)
            print(f"Registered agent: {args.name}")
        elif args.action == "mode" and args.name and args.mode:
            kernel.agent_manager.set_mode(args.name, AgentMode(args.mode))
            print(f"Agent {args.name} mode -> {args.mode}")
    elif args.command == "scheduler":
        if args.action == "list":
            for m in kernel.scheduler.get_all_missions():
                print(f"  {m.id[:8]}  {m.name:<20}  enabled={m.enabled}  runs={m.run_count}")
        elif args.action == "due":
            due = kernel.scheduler.get_due_missions()
            print(f"Due missions: {len(due)}")
    else:
        parser.print_help()


if __name__ == "__main__":
    main()
